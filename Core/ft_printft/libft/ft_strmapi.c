#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	s_length;
	size_t	i;
	char	*result;

	if (!s || !f)
		return (NULL);
	s_length = ft_strlen(s);
	result = (char *)malloc(s_length + 1);
	if (!result)
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
